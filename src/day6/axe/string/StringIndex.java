package day6.axe.string;

public class StringIndex {
	
	public static void main(String[] args) {
		String name="GreensTechnology";
		int c=name.indexOf("o");
		System.out.println(c);
		
		String sel="seleniumAutomationtool";
		int e=sel.indexOf("o");
		System.out.println(e);
		
		String vel="velmurugan";
		int f=vel.indexOf("n");
		System.out.println(f);
		
		String jav="J A V A P R O G R A M";
		int g=jav.indexOf(" ");
		System.out.println(g);
				
		String num="9095484678";
		int n=num.indexOf("8");
		System.out.println(n);
	}

}
